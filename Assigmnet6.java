package Assignment6_staticmethods;
public class StaticMethods {
public static void main(String[] args) {
	operators();
}
public static void operators() {
	int a=10;
	int b=20;
	System.out.println(a==b);
	System.out.println(a!=b);
	System.out.println(a>b);
	System.out.println(a<b);
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(a/b);
	System.out.println(a|b);
	System.out.println(a&b);
	
}
}

